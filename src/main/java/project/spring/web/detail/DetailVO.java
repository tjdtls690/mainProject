package project.spring.web.detail;

public class DetailVO {
	private int item_code;
	private double item_avg_star;
	private int count;
	
	public int getItem_code() {
		return item_code;
	}
	public void setItem_code(int item_code) {
		this.item_code = item_code;
	}
	public double getItem_avg_star() {
		return item_avg_star;
	}
	public void setItem_avg_star(double item_avg_star) {
		this.item_avg_star = item_avg_star;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
}
